    private void listsComboBoxPopupMenuWillBecomeVisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_listsComboBoxPopupMenuWillBecomeVisible
        listsComboBox.removeAllItems();
        String[] blackLists;
        if(this.isRescuer){
            blackLists = BlackListFunctions.getRescuerLists(this.personId);
        }
        else{
            blackLists = BlackListFunctions.getAssocciationLists(this.personId);
        }
        for(int i = 0; i < blackLists.length; i++){
                listsComboBox.addItem(blackLists[i]);
        }
        
    }//GEN-LAST:event_listsComboBoxPopupMenuWillBecomeVisible

