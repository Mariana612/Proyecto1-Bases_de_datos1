    public static boolean checkEmail(String address){
        String regExpression = "^[A-Za-z0-9+_.-]+@(.+)$";
        Pattern pattern = Pattern.compile(regExpression);
        Matcher checkMatch = pattern.matcher(address);
        return checkMatch.matches();
    }

